create table if not exists public.profiles (
  id uuid primary key references auth.users(id) on delete cascade,
  name text,
  email text,
  created_at timestamptz not null default now()
);

create table if not exists public.exercises (
  id bigint generated by default as identity primary key,
  user_id uuid not null default auth.uid() references auth.users(id) on delete cascade,
  name text not null,
  primary_muscle text not null,
  secondary_muscle text,
  is_strength_exercise integer not null default 0,
  created_at timestamptz not null default now()
);

create table if not exists public.workout_sessions (
  id bigint generated by default as identity primary key,
  user_id uuid not null default auth.uid() references auth.users(id) on delete cascade,
  workout_date date not null,
  notes text,
  created_at timestamptz not null default now()
);

create table if not exists public.workout_sets (
  id bigint generated by default as identity primary key,
  user_id uuid not null default auth.uid() references auth.users(id) on delete cascade,
  session_id bigint not null references public.workout_sessions(id) on delete cascade,
  exercise_id bigint not null references public.exercises(id) on delete cascade,
  set_number integer not null,
  reps integer not null,
  weight numeric not null,
  rir numeric,
  created_at timestamptz not null default now()
);

create table if not exists public.body_weight_logs (
  id bigint generated by default as identity primary key,
  user_id uuid not null default auth.uid() references auth.users(id) on delete cascade,
  weight numeric not null,
  logged_date date not null,
  created_at timestamptz not null default now(),
  unique (user_id, logged_date)
);

create table if not exists public.muscle_strength_config (
  id bigint generated by default as identity primary key,
  user_id uuid not null default auth.uid() references auth.users(id) on delete cascade,
  muscle_group text not null,
  exercise_id bigint not null references public.exercises(id) on delete cascade,
  weight_factor numeric not null default 1.0
);

create table if not exists public.app_settings (
  user_id uuid not null default auth.uid() references auth.users(id) on delete cascade,
  key text not null,
  value text not null,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  primary key (user_id, key)
);

