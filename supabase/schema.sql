create policy "profiles are visible to their owner" on public.profiles
  for select using (auth.uid() = id);

create policy "profiles can be inserted by their owner" on public.profiles
  for insert with check (auth.uid() = id);

create policy "profiles can be updated by their owner" on public.profiles
  for update using (auth.uid() = id) with check (auth.uid() = id);

create policy "exercises are owned by their user" on public.exercises
  for all using (auth.uid() = user_id) with check (auth.uid() = user_id);

create policy "workout sessions are owned by their user" on public.workout_sessions
  for all using (auth.uid() = user_id) with check (auth.uid() = user_id);

create policy "workout sets are owned by their user" on public.workout_sets
  for all using (auth.uid() = user_id) with check (auth.uid() = user_id);

create policy "body weight logs are owned by their user" on public.body_weight_logs
  for all using (auth.uid() = user_id) with check (auth.uid() = user_id);

create policy "muscle configs are owned by their user" on public.muscle_strength_config
  for all using (auth.uid() = user_id) with check (auth.uid() = user_id);

create policy "settings are owned by their user" on public.app_settings
  for all using (auth.uid() = user_id) with check (auth.uid() = user_id);

